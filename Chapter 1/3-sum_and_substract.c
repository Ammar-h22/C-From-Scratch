# include <stdio.h>

int main()
{
   int a = 5;
   int d = 10;
   printf("Sum of a and d is %d",a+d);
   return 0;
}

// Or there is also another way to add 2 integers:

#include <stdio.h>

int main()
{
    int a = 5;
    int d = 10;
    int e = a + d;
    printf("Sum of a and d is %d", e);
    return 0;
}

// Also like sum we can also do substract with integers:

# include <stdio.h>

int main()
{
   int a = 5;
   int d = 10;
   printf("Substraction of a and d is %d",a-d);
   return 0;
}


# include <stdio.h>


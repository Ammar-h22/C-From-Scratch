// This is an inbuilt function named (pow) in C to find power of any number.
// For example if we need to find 3 raise to the power 5 , so for this we have a (pow) function .

#include <stdio.h>
#include <math.h>

int main()
{
    printf("The value of 3 to the power 5 is : %f", pow(3, 5));
    return 0;
}
